class Solution {
  public:
    Node* constructLL(vector<int>& arr) {
        // code here
        Node* head = new Node(arr[0]);    //1st element of array will be our head
        Node* mover = head;               //initially mover will be pointing to head
        for(int i = 1; i<arr.size(); i++) // then we skip the 1st element at 0th index and go till last to create link between all nodes
        {
            Node* temp = new Node(arr[i]); // store value in temp
            mover->next = temp;            //to create link we want mover(which is currently at head during 1st iteration) to point to this temp(the next ele to head in 1st iter)
            mover = temp ;  // or mover = mover->next;  now we want mover to go frwd 
        }
        return head;
    }
};

